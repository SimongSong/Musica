export const escFunction = (event) => {
  if (event.keyCode === 81) {
    //Q pressed
    console.log("Q");
  } else if (event.keyCode === 87) {
    //W pressed
    console.log("W");
  } else if (event.keyCode === 69) {
    //E pressed
    console.log("E");
  } else if (event.keyCode === 82) {
    //R pressed
    console.log("R");
  } else if (event.keyCode === 27) {
    //Esc pressed
    console.log("Esc");
  }
};
